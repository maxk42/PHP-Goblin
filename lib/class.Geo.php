	static function getGeoByIP($ip) {
		exec('whois ' . $ip, $results);

		$data = array();
		foreach($results as $result) {
			$result = explode(':', $result, 2);
			if($result[0][0] == '#')	// does the result begin with a hash?
				$data['(comments)'][] = $result[0];
			else if($result[0] != '') {
				if(count($result) == 2)
					$data[trim($result[0])] = trim($result[1]);
				else
					$data[] = trim($result[0]);
			}
		}

		$geo['countryCode'] = $data['Country'];
		$geo['countryName'] = Geo::getCountryByCode($data['Country']);
		$geo['province'] = $data['StateProv'];
		$geo['city'] = $data['City'];
		$geo['postalCode'] = $data['PostalCode'];

		return $geo;	// return geographic array
	}
	
	static function validCountryCode($code) {
		return array_key_exists($code, self::$countryCodes);
	}
	
	static function getCountryByCode($code) {
		// convert a two-digit country code to a country name
		$code = strtoupper($code);
		if(!isset(self::$countryCodes[$code]))
			return null;
		return self::$countryCodes[$code];
	}
